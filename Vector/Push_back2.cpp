// the process to store data in vector is called pushback 

#include<bits/stdc++.h>
using namespace std;
int main ()
{
     vector<int>v;
     
     v.push_back(1);  //  v[0]
     v.push_back(2);  //  v[1]
     v.push_back(3);  //  v[2]
     v.push_back(4);  //  v[3]
     v.push_back(5);  //  v[4]

     // we can store this way also
   //indexes       0 1 2 3 4
     vector<int>v={1,2,3,4,5};

     // then we can print them using for loop.
     // each and every case we need size as condition in loop

}